     */
    public function update(Request $request, $id)
    {
        if($request['status'] == 1){
          $pivot_data = ['fixed' => true];
        }else{
          $pivot_data = ['accepted' => NULL, 'fixed' => true];
        }

        $member = Member::find( auth()->user()->userable->id );

        $member->courseClasses()->syncWithoutDetaching([
          $id => $pivot_data
        ]);

        return redirect()->back();
    }

